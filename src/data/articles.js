/**
 * Calculate relevance score for an article given the user's context
 */
export const getArticleRelevance = (article, lifeStage, priorities = [], cyclePhase = null) => {
  let score = 0;
  let label = null;

  if (article.lifeStages?.includes(lifeStage)) {
    score += 3;
    label = 'Para a tua fase';
  }
  if (cyclePhase && article.cyclePhases?.includes(cyclePhase)) {
    score += 2;
    label = label || 'Para este momento';
  }
  priorities.forEach(p => {
    if (article.priorities?.includes(p)) {
      score += 2;
      label = label || 'Para as tuas prioridades';
    }
  });

  return { score, label };
};

/**
 * Returns articles sorted by relevance for the given context
 */
export const getPersonalizedArticles = (lifeStage, priorities = [], cyclePhase = null) => {
  return articles
    .map(a => {
      const { score, label } = getArticleRelevance(a, lifeStage, priorities, cyclePhase);
      return { ...a, relevanceScore: score, relevanceLabel: label };
    })
    .sort((a, b) => b.relevanceScore - a.relevanceScore);
};
